SELECT B.CNTRY_NAME AS KRAJ, SDO_GEOM.SDO_LENGTH(SDO_GEOM.SDO_INTERSECTION(A.GEOM, B.GEOM, 1), 1, 'unit=km') AS ODLEGLOSC FROM COUNTRY_BOUNDARIES A, COUNTRY_BOUNDARIES B
WHERE SDO_FILTER(A.GEOM, B.GEOM) = 'TRUE' AND A.CNTRY_NAME = 'Poland' AND B.CNTRY_NAME != 'Poland';

--B)
SELECT CNTRY_NAME FROM COUNTRY_BOUNDARIES WHERE SDO_GEOM.SDO_AREA(GEOM, 1, 'unit=SQ_KM') = (SELECT MAX(SDO_GEOM.SDO_AREA(GEOM, 1, 'unit=SQ_KM')) FROM COUNTRY_BOUNDARIES);

--C)
SELECT SDO_GEOM.SDO_AREA(
    SDO_GEOM.SDO_MBR(SDO_GEOM.SDO_UNION(A.GEOM, B.GEOM, 1)), 1, 'unit=SQ_KM'
) AS SQ_KM FROM MAJOR_CITIES A, MAJOR_CITIES B WHERE A.CITY_NAME = 'Warsaw' AND B.CITY_NAME = 'Lodz';

--D)
SELECT SDO_GEOM.SDO_UNION(A.GEOM, B.GEOM, 1).GET_GTYPE() AS GTYPE FROM COUNTRY_BOUNDARIES A, MAJOR_CITIES B WHERE A.CNTRY_NAME = 'Poland' AND B.CITY_NAME = 'Prague';

--E)
SELECT A.CITY_NAME, A.CNTRY_NAME FROM MAJOR_CITIES A, COUNTRY_BOUNDARIES B WHERE A.CNTRY_NAME = B.CNTRY_NAME AND
SDO_GEOM.SDO_DISTANCE(A.GEOM, SDO_GEOM.SDO_CENTROID(B.GEOM, 1), 1) = (SELECT MIN(SDO_GEOM.SDO_DISTANCE(A.GEOM, SDO_GEOM.SDO_CENTROID(B.GEOM, 1), 1)) FROM MAJOR_CITIES A, COUNTRY_BOUNDARIES B WHERE A.CNTRY_NAME = B.CNTRY_NAME);

--F)
SELECT A.NAME, SUM(SDO_GEOM.SDO_LENGTH(SDO_GEOM.SDO_INTERSECTION(A.GEOM, B.GEOM, 1), 1, 'unit=km')) AS DLUGOSC
FROM RIVERS A, COUNTRY_BOUNDARIES B WHERE B.CNTRY_NAME = 'Poland' AND SDO_RELATE(A.GEOM, B.GEOM, 'mask=ANYINTERACT') = 'TRUE'
GROUP BY A.NAME;