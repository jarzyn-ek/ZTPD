SELECT A.CITY_NAME AS MIASTO, SDO_NN_DISTANCE(1) AS ODL FROM MAJOR_CITIES A, MAJOR_CITIES B
WHERE SDO_NN(A.GEOM, B.GEOM, 'sdo_num_res=10 unit=km', 1) = 'TRUE' AND B.CITY_NAME = 'Warsaw' AND A.CITY_NAME != 'Warsaw';

--B)
SELECT A.CITY_NAME AS MIASTO FROM MAJOR_CITIES A, MAJOR_CITIES B
WHERE SDO_WITHIN_DISTANCE(A.GEOM, B.GEOM, 'distance=100 unit=km') = 'TRUE' AND B.CITY_NAME = 'Warsaw' AND A.CITY_NAME != 'Warsaw';

--C)
